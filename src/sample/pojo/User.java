package sample.pojo;

public class User {
    private double Ki;
    private double Yi;


    public User(double Ki, double Yi) {
        this.Ki = Ki;
        this.Yi = Yi;

    }

    public User() {
    }

    public double getKi() {
        return Ki;
    }

    public void setKi(double Ki) {
        this.Ki = Ki;
    }

    public double getYi() {
        return Yi;
    }

    public void setYi(double Yi) {
        this.Yi = Yi;
    }


}
